#include <unistd.h>
void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	main(int argc, char **argv)
{
	while (argv)
		break;
	ft_putchar(argc + 47);
	ft_putchar(10);
	return (0);
}
